        $(document).ready(function () {
            $.get("<?= base_url('Admin_ctrl/get_like_count') ?>", function (response) {
                let res = JSON.parse(response);
                $("#likeCount").text(res.like_count);
            });

            $.get("<?= base_url('Admin_ctrl/get_feedbacks') ?>", function (response) {
                let res = JSON.parse(response);
                $("#feedbackCount").text(res.feedbacks.length);
                displayFeedback(res.feedbacks);
            });

            $("#likeBtn").click(function () {
                $.post("<?= base_url('Admin_ctrl/like') ?>", function (response) {
                    let res = JSON.parse(response);
                    if (res.status === 'success') {
                        $("#likeCount").text(res.like_count);
                    } else {
                        alert(res.message);
                    }
                });
            });

            $("#feedbackModal").on("shown.bs.modal", function () {
                fetchFeedbacks();
            });

            $("#feedbackForm").submit(function (event) {
                event.preventDefault();
                let rating = $("#rating").val();
                let comment = $("#comment").val();

                $.post("<?= base_url('Admin_ctrl/feedback') ?>", { rating: rating, comment: comment }, function (response) {
                    let res = JSON.parse(response);
                    if (res.status === 'success') {
                        Swal.fire({
                            icon: 'success',
                            title: 'Feedback Submitted!',
                            text: res.message,
                            confirmButtonText: 'OK'
                        }).then(() => {
                            $("#feedbackForm")[0].reset();
                            fetchFeedbacks();
                        });
                    } else {
                        Swal.fire({
                            icon: 'error',
                            title: 'Error',
                            text: res.message
                        });
                    }
                });
            });


            function fetchFeedbacks() {
                $.getJSON("<?= base_url('Admin_ctrl/get_feedbacks') ?>", function (response) {
                    displayFeedback(response.feedbacks);
                });
            }

            function displayFeedback(feedbacks) {
                $("#feedbackList").empty();
                if (feedbacks.length === 0) {
                    $("#feedbackList").append("<p class='text-muted'>No feedback available.</p>");
                    return;
                }

                feedbacks.forEach(function (fb) {
                    let stars = "".repeat(fb.rating) + "".repeat(5 - fb.rating);

                    $("#feedbackList").append(`
            <div class="border p-2 mb-2 rounded">
                <strong>${fb.name}</strong> 
                <small class="text-muted">(${stars})</small><br>
                <small class="text-muted">${fb.created_at}</small>
                <p>${fb.comment}</p>
            </div>
        `);
                });
            }
        });
    </script>